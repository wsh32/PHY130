#A space probe is fired as a projectile from the Earth's surface with an initial speed of 2.32  104 m/s. What will its speed be when it is very far from the Earth? Ignore atmospheric friction and the rotation of the Earth.

v = 2.32e4;

G = 6.67e-11;
M = 5.97e24;
r = 6.371e6;
vf = sqrt(-2*G*M/r+v^2)